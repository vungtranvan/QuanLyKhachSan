    public UpdateDichVu(DichVu dv, CallBackUpdate _cb, ResourceBundle rb) {
        initComponents();
        dichVuDAO = new DichVuDAO();

        modelLoaiDichVu = new DefaultComboBoxModel();
        loaiDichVuDAO = new LoaiDichVuDAO();
        lstLoaiDichVu = loaiDichVuDAO.getAll();

        modelDonVi = new DefaultComboBoxModel();
        donViDAO = new DonViDAO();
        lstDonVi = donViDAO.getAll();

        resetText();
        this.cb = _cb;
        initDataLoaiDichVu();
        initDataDonVi();

        txtMaDVu.setText(dv.getMaDichVu());
        txtDonGia.setText(Float.toString(dv.getDonGia()));

        String maLDVu = dv.getMaLoaiDichVu();
        LoaiDichVu lDVu = lstLoaiDichVu.stream().filter(x -> x.getMaLoaiDichVu().equals(maLDVu)).findAny().orElse(null);
        modelLoaiDichVu.setSelectedItem(lDVu);

        String maLP = dv.getMaDonVi();
        DonVi dvi = lstDonVi.stream().filter(x -> x.getMaDonVi().equals(maLP)).findAny().orElse(null);
        modelDonVi.setSelectedItem(dvi);
        this.rb = rb;
        translate(this.rb);
    }

    public void initDataLoaiDichVu() {
        for (LoaiDichVu adv : lstLoaiDichVu) {
            modelLoaiDichVu.addElement(adv);
        }
        jcbMaLoaiDVu.setModel(modelLoaiDichVu);
    }

    public void initDataDonVi() {
        for (DonVi adv : lstDonVi) {
            modelDonVi.addElement(adv);
        }
        jComboBoxMaDVi.setModel(modelDonVi);
    }

    public void resetText() {
        txtDonGia.setText("");
        txtErrorDonGia.setText("");
    }

