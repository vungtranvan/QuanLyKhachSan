    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        Boolean check = true;
        String maDV = txtMaDVu.getText();

        LoaiDichVu lp = (LoaiDichVu) modelLoaiDichVu.getSelectedItem();
        String ma_LoaiDichVu = lp.getMaLoaiDichVu();

        DonVi dv = (DonVi) modelDonVi.getSelectedItem();
        String ma_DonVi = dv.getMaDonVi();

        String gia = txtDonGia.getText();

        Float giaInput = null;
        if (gia.length() <= 0) {
            txtErrorDonGia.setText("Giá không được để trống");
            check = false;
        } else {
            txtErrorDonGia.setText("");
            try {
                giaInput = Float.parseFloat(gia);
                if (giaInput < 0) {
                    txtErrorDonGia.setText("Giá phải lớn hơn hoặc bằng 0");
                    check = false;
                }
            } catch (Exception e) {
                txtErrorDonGia.setText("Giá phải lớn hơn hoặc bằng 0");
                check = false;
            }
        }
        if (check == true) {
            int row = dichVuDAO.update(new DichVu(maDV, ma_LoaiDichVu, ma_DonVi, giaInput));
            if (row > 0) {
                JOptionPane.showMessageDialog(rootPane, "Cập nhật thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                resetText();
                cb.doUpdate();
                dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Cập nhật thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed


