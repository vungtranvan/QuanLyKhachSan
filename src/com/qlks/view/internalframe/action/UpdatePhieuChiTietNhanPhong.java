    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        Boolean check = true;
        LocalDate dateTT = null;
        if (txtNgayTraThucTe.getDate() != null) {
            dateTT = txtNgayTraThucTe.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        } else {
            txtErrorNgayTraThucTe.setText("Ngày trả thực tế ko được để trống");
            check = false;
        }

        if (check == true) {
            ChiTietPhieuNhanPhong ct = new ChiTietPhieuNhanPhong(ma_NhanPhong, dateTT);

            int count = chiTietPhieuNhanPhongDAO.updateNgayTraDuKien(ct);

            if (count > 0) {
                JOptionPane.showMessageDialog(rootPane, "Cập nhật thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                resetText();
                cb.doUpdate();
                dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Cập nhật thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed


