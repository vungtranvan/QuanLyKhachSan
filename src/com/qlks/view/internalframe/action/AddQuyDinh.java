    private void btnThemMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMoiActionPerformed
        Boolean check = true;
        String tenQD = txtTenQD.getText();
        String moTa = txtMoTa.getText();

        if (tenQD.length() <= 0) {
            txtErrorTenQD.setText("Không được để trống");
            check = false;
        } else if (tenQD.length() > 50) {
            txtErrorTenQD.setText("Tên quy định có tối đa là 50 ký tự");
            check = false;
        } else {
            txtErrorTenQD.setText("");
        }

        if (moTa.length() <= 0) {
            txtErrorMoTa.setText("Không được để trống");
            check = false;
        }

        if (check == true) {
            int row = quyDinhDAO.add(new QuyDinh(tenQD, moTa));
            if (row > 0) {
                JOptionPane.showMessageDialog(rootPane, "Thêm thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                resetText();
                cb.doAdd();
                dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Thêm thất bại", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnThemMoiActionPerformed


